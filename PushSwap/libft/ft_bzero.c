#include "libft.h"

void	ft_bzero(void *s, size_t n);

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*temp_s;

	temp_s = (char *)s;
	i = n;
	while (i-- > 0)
	{
		temp_s[i] = 0;
	}
}
